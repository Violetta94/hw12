public class Author {
    String nameAuthor;
    String familiAuthor;

    public Author (String nameAuthor, String familiAuthor) {
        this.nameAuthor = nameAuthor;
        this.familiAuthor = familiAuthor;
    }

    public String getNameAuthor() {
        return nameAuthor;
    }

    public String getFamiliAuthor() {
        return familiAuthor;
    }
}
